package BootCampReveiw;

public class Loops {

	public static void main(String[] args) {
		
		for(int i=0; i < 5; i+=2  ) {
			System.out.println("Hello Batch 12");
		}
		
		System.out.println("=======================");
		
		int z =5;
		while(z > 0) {
			System.out.println("Hola Batch 12");
			z--;
		}
		
		System.out.println("=======================");
		
		do {
			System.out.println("Morning Batch 12");
		}while(false);
		
		System.out.println("=======================");
		
		int[] nums = {1,2,3,4,5};
		for( int each : nums) {
			System.out.println(each);
		}
		
		
		
		
		
		
		
		
		
		
	}
}
